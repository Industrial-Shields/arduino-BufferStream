#include "ReadBufferStream.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
ReadBufferStream::ReadBufferStream(void *buff, size_t len) : BaseBufferStream(buff, len) {
}

////////////////////////////////////////////////////////////////////////////////////////////////////
int ReadBufferStream::available() {
	return len;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
int ReadBufferStream::read() {
	if (len == 0) {
		return -1;
	}
	--len;
	return *buff++;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
int ReadBufferStream::peek() {
	if (len == 0) {
		return -1;
	}
	return *buff;
}
